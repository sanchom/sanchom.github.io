◊title{Canadian Aviation Regulations}
◊heading{About this book}

This book will help prepare you for Canada's Private Pilot written
exam (aeroplane). It will not present all the material you need to
know; it will present a lot of it, but moreso, it will teach you how
to understand the references that you yourself can use to prepare for
the exam.

Your instructor can do little more than set you up to be able to study
well. You literally just need to memorize the material. To help you
with that memorization, this book will help you understand the
structure of the regulations, relate regulations to specific
scenarios, and give you effective study techniques for memorization.

The regulations and topics that you're expected to know are listed in
◊a[#:href
"https://www.tc.gc.ca/eng/civilaviation/publications/tp12880-air_law-menu-1226.htm"]{Transport
Canada's Study and Reference Guide}. Everything you'll need to work
through this book is available online. You'll find links to related
material throughout the book. A full list of references are available
in the appendix, but 99% of the material comes straight from the
◊em{Canadian Aviation Regulations} and the ◊em{Aeronautical
Information Manual}.

◊heading{How to study the Canadian Aviation Regulations}

One way to study for this section of the written exam is to just look
at each section listed in the Study and Reference Guide and memorize
it. In the end, that's what all preparation methods get you to do, but
if somehow you have an exceptional memory or your own methods that get
you there, it really could be that simple. But, that's a bit like
saying, "just be smart."

When I was studying for the Commercial Pilot written examination, I
copy-and-pasted every relevant section from the CARs into a document
and printed it out. With small text and small margins it took about
fifty double-sided pages. Through this exercise of browsing the CARs,
copy-and-pasting, and then reading through them on my own again, I
memorized a lot of the material. It might be useful for you to do
something like that, but if you want to skip to the end result, here's
a recently-compiled set of CARs that are referenced by the Study and
Reference Guide.

◊em{Spaced reptition} is a method of study that is especially useful
for large-scale memorization tasks. There are many free software
programs that you can use to help you with this. I've used Anki. (Not
for studying the CARs though; I learned about Anki long after I had
finished my licences.) I've prepared a deck of CARs cards that you can
import into Anki, but I think it would be more useful for you to do
this yourself. Every chance to look at the material from a different
perspective is a chance to better learn the material.

This book will also give you those chances.

◊heading{Structure of the Canadian Aviation Regulations}

◊sub-heading{Parts}
The CARs are organized into eight parts, but only five are relevant
for your private pilot written examination:

◊itemize{

Part I (General Provisions): definitions, mostly.

Part II (Aircraft Identification and Registration): certificate of registration.

Part III (Aerodromes and Airports): Lighting, markings.

Part IV (Personnel Licensing and Training): Prerequisites, medical requirements, recency.

Part VI (General Operating and Flight Rules): Airspace, altitude
minima, collision avoidance, equipment requirements, VFR procedures,
etc.

}

◊sub-heading{Subparts and divisions}

Each part is further broken down into subparts and divisions, but I
never found those distinctions useful for helping me understand the
material for the Private Pilot licence. ◊margin-note{Subparts ◊em{are} an useful distinction in Part VII, where
Subpart 2 (702.xxx) is about aerial work, Subpart 3
(703.xxx) is about air taxi operations, Subpart 4
(704.xxx) is about commuter operations, and Subpart
5 (705.xxx) is about airline operations.}

◊sub-heading{Sections}

The basic unit of regulation is a ◊em{Section}. For example, this is
section 602.01:

◊q{◊reg[#:sec 602.01]}

The number, 602.01, indicates that this is the first section in
Subpart 2 of Part VI of the CARs.

◊sub-heading{Definitions}

602.01 refers to "aircraft". What things are considered "aircraft"?
You can probably guess that Cessnas are aircraft. But what about
gliders, balloons, rockets, drones? Are these all aircraft too?

Whenever you are looking for a definition, search "inside-out". Start
at the section itself and see if the term is defined there. If it is,
great. If not, move on to the enclosing Division, then Subpart, then
Part, and then 101.01 (the CARs-wide definitions).

Let's continue with this example and search for the definition of
aircraft. The section itself doesn't define "aircraft".

So, let's move to the enclosing division (Part VI, Subpart 2, Division
1). There are no definitions there.

Next, on to the enclosing ◊em{subpart} (Part VI, Subpart 2). There are also
no definitions there.

When we look to the enclosing ◊em{part} (Part VI), we finally find
some definitions:

◊q{◊b{600.01} In this Part,

◊bem{ADIZ} or ◊bem{Air Defence Identification Zone} means the airspace
that extends upward from the surface in those areas of Canada and off
the coasts of Canada, the boundaries of which are specified in the
◊em{Designated Airspace Handbook};

◊elide
}

Unfortunately, "aircraft" isn't defined there. There's one last place
in the CARs where "aircraft" might be defined: the catch-all 101.01.

◊q{
◊b{101.01 (1)} In these Regulations,

◊bem{ACAS} or ◊bem{Airborne Collision Avoidance System} means ◊elide;

◊bem{Act} means ◊elide;

◊bem{advanced ultra-light aeroplane} means ◊elide;

◊elide

◊bem{aeroplane} means a power-driven heavier-than-air aircraft that
derives its lift in flight from aerodynamic reactions on surfaces that
remain fixed during flight;

◊elide
}

It looks like the CARs defines "aeroplane", but not
"aircraft". Transport Canada uses the term "aircraft" throughout the
CARs, but doesn't actually define it. This can't be right.

I've misled you. There is one more place
where a definition might be found: the ◊em{Aeronautics Act}. ◊margin-note{◊em{◊a[#:href
"http://laws-lois.justice.gc.ca/eng/acts/A-2/FullText.html"]{Aeronautics
Act}}. R.S.C., 1985, c.◊poly-string->symbol{nbsp}A◊poly-string->number{8209}2} It's what
gives Transport Canada the authority to create the CARs and it also
has a few definitions. In the ◊em{Aeronautics Act}, we learn what an aircraft is:

◊q{
◊bem{aircraft} means ◊elide any machine capable of deriving support
in the atmosphere from reactions of the air, and includes a rocket;
}

It turns out "aircraft" is a very broad concept. Gliders are
aircraft. Drones are aircraft. Rockets are aircraft.

